package prak2operatorlanjut;

public class Main {

    public static void main(String[] args) {
        int a=10; int b=4;
        int c=0; int d=0;
        int counter=1;
        
        a++;
        b--;
        c+=counter;
        d-=counter;
        
        System.out.println("Nilai a+1: "+a);
        System.out.println("Nilai b-1: "+b);
        System.out.println("Nilai c+counter: "+c);
        System.out.println("Nilai d-counter: "+d);
    }
    
}
